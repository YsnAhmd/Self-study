//Primitive datatype(definition)
//Boolean
let bool = true;
console.log(typeof bool);
//Number
let num = 46;
console.log(typeof num);
//String
let str = 'true';
console.log(typeof str);
//Null
let nul = null;
console.log(typeof nul); //Shows data type as object which is considere a bug in JS
//Symbol
let sym = Symbol(); //without using parentheses() symbol will work as a function
console.log(typeof sym);
//Undefined
let und = undefined;
console.log(typeof und);


//Complex datatypes(Definition)
//Object
